void solve()
{
    int n;
    cin >> n;
    int XOR = n;
    for (int i = 1; i < n; i++)
    {
        int t;
        cin >> t;
        XOR = XOR ^ t;
        XOR = XOR ^ i;
    }
    cout << XOR;
}
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    while (t--)
    {
        solve();
    }
    return 0;
}
