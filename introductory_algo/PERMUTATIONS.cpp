void solve()
{
    int n;
    cin >> n;
    if (n > 1 && n <= 3)
        cout << "NO SOLUTION\n";
    else
    {
        for (int i = n; i >= 1; i--)
            if (i & 1)
                cout << i << " ";
        for (int i = n; i >= 1; i--)
            if (i % 2 == 0)
                cout << i << " ";
    }
}
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    while (t--)
    {
        solve();
    }
    return 0;
}
