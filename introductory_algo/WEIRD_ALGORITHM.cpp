void func(ll n)
{
    cout << n << " ";
    if (n == 1)
        return;
    if (n % 2)
        func(n * 3 + 1);
    else
        func(n / 2);
}
void solve()
{
    ll n;
    cin >> n;
    func(n);
}
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    while (t--)
    {
        solve();
    }
    return 0;
}
